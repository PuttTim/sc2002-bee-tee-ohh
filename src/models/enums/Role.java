package models.enums;

/**
 * <p>Represents the different roles in the system.</p>
 */
public enum Role {
    /**
     * A user who applies for a housing project.
     */
    APPLICANT,

    /**
     * A user responsible for managing projects and applications.
     */
    OFFICER,

    /**
     * A user who oversees and manages the overall operations.
     */
    MANAGER
}
